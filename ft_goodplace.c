char *ft_goodplace(char *map, char *str, char c, int j)
{
	int i;

	i = 0;
	while (map[i])
	{
		if (str[i] == c)
		{
			str[i] = '.';
			map[i] = c;
		}
		i++;
	}
	return (map);
}
